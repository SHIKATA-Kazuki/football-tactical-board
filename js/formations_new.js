export function getFormationName(backs, volante, top) {
    if (backs > 0.5) {
        if (volante > 0.5) {
            if (top < 0.5) { return "4231";} 
            else {return "442";}
        } else {
            if (top < 0.5) { return "4123";} 
            else {return "442◇";}
        }
    } else {
        if (volante > 0.5) {
            if (top < 0.5) {return "3421";} 
            else {return "352W";}
        } else {
            if (top < 0.5) {return "343◇";} 
            else {return "352M";}
        }
    }
}